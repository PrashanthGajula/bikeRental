package com.prashanth.bikeRental.model.rest;

public class RESTUser
{
    private String name;
    private long phone;
    private String email;
    private String isAdmin;

    public String getName()
    {
        return name;
    }

    public long getPhone()
    {
        return phone;
    }

    public String getEmail()
    {
        return email;
    }

    public String getIsAdmin()
    {
        return isAdmin;
    }
}
